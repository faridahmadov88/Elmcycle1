package section8_3;

public class Person {
	String name;
	int age;
	
	
	
	public void setName() {
		this.name=name;
	}
	public String getName() {
		return this.name;
	}
	public void setAge() {
		this.age=age;
	}
	public int getAge() {
		return this.age;
	}
	
	
	
	
	public Person (String name, int age) {
		this.name=name;
		this.age=age;
	}

}
